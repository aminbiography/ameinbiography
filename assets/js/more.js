/* Read More SEO Start */
function myFunctionMoreSeo() {
  var dots = document.getElementById("dots-seo");
  var moreTextSeo = document.getElementById("more-seo");
  var btnTextSeo = document.getElementById("more-text-seo");

  if (dots.style.display === "none") {
    dots.style.display = "inline";
    btnTextSeo.innerHTML = "Read more";
    moreTextSeo.style.display = "none";
  } else {
    dots.style.display = "none";
    btnTextSeo.innerHTML = "Read less";
    moreTextSeo.style.display = "inline";
  }
}
/* Read More SEO End */

